    private void LoadSubjectToCBO() {

        SubjectDAO dao = new SubjectDAO();
        ResultSet rs = dao.loadData();
        try {
            while (rs.next()) {
                cbsuject.addItem(rs.getString("tenmonhoc"));
            }
            rs.close();

        } catch (Exception e) {
            System.out.println("GUI.GiaoVien.Question.LoadSubjectToCBO() : " + e.getMessage());
        }
    }

    public void loadDataToTable() {
        model = new DefaultTableModel(null, new Object[]{"Question ID", "Question Content", "Level", "Aswer A", "Answer B",
            "Answer C", "Answer D", "CorrectAnswer", "Subject ID", "User ID", "Status"});
        QuestionDAO dao = new QuestionDAO();
        ResultSet rs = dao.loadData();
        try {

            while (rs.next()) {
                model.addRow(new Object[]{rs.getInt("macauhoi"), rs.getString("ndcauhoi"), rs.getInt("dokho"),
                    rs.getString("phuonganA"), rs.getString("phuonganB"), rs.getString("phuonganC"), rs.getString("phuonganD"), rs.getString("dapan"), rs.getString("mamonhoc"), rs.getInt("matk"),
                    rs.getInt("trangthai")});
            }
            tblques.setModel(model);
            rs.close();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

