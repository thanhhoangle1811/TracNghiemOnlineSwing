    public void readExcel() {
        JFileChooser dialog = new JFileChooser();
        int dialogResult = dialog.showOpenDialog(null);
        if (dialogResult == JFileChooser.OPEN_DIALOG) {
            String filePath = dialog.getSelectedFile().getPath();
            try {
                FileInputStream input = new FileInputStream(filePath);
                POIFSFileSystem fs = new POIFSFileSystem(input);
                HSSFWorkbook wb = new HSSFWorkbook(fs);
                DataFormatter formatter = new DataFormatter(Locale.US);
                HSSFSheet sheet = wb.getSheetAt(0);
                Row row;
                for (int i = 1; i <= sheet.getLastRowNum(); i++) {

                    row = sheet.getRow(i);

                    String question = formatter.formatCellValue(row.getCell(0));
                    int quesLevel = Integer.parseInt(formatter.formatCellValue(row.getCell(1)));
                    String subjectID = formatter.formatCellValue(row.getCell(2));
                    String anSw1 = formatter.formatCellValue(row.getCell(3));
                    String anSw2 = formatter.formatCellValue(row.getCell(4));
                    String anSw3 = formatter.formatCellValue(row.getCell(5));
                    String anSw4 = formatter.formatCellValue(row.getCell(6));
                    String anSwCorrect = formatter.formatCellValue(row.getCell(7));
                    int userID = Integer.valueOf(formatter.formatCellValue(row.getCell(8)));

                    DTO.Question dto = new DTO.Question(question, quesLevel, subjectID, anSw1, anSw2, anSw3, anSw4, anSwCorrect, userID);
                    QuestionDAO dao = new QuestionDAO();
                    dao.inserQuestion(dto);
                }
                alt.alter("Success import excel to mysql table");
                loadDataToTable(); // sau khi update thanh cong se load du lieu len bang
                input.close();
                System.out.println("Success import excel to mysql table");
            } catch (IOException e) {
                System.out.println("err : " + e.getMessage());
            }

        }
    }

